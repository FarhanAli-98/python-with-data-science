# String concatenation
str1 = "Hello"
str2 = "World"
result = str1 + ", " + str2 + "!"
print(result)

# String repetition
print(str1 * 3)

# String length
print(f"Length of '{str1}': {len(str1)}")

# String slicing
print(f"First three characters: {str1[:3]}")
print(f"Last two characters: {str1[-2:]}")
